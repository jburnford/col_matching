def colony_targets(place: str | None, data_dir: str) -> set[str]:
    """Normalized graph-colony names a printed place can correspond to."""
    canonical = lookup(place or "", data_dir) or (place or "")
    n = norm(canonical)
    if not n:
        return set()
    targets = PLACE_ALIASES.get(n, {n}) | {n}
    roll = _load_rollup(data_dir).get(n)
    if roll:
        targets.add(roll)
    return targets


_TRAILING_NOISE = re.compile(r"\s+(colony|col|protectorate|prot|terr)\.?$", re.I)
# dotted office abbreviations, matched case-sensitively in clause text
_OFFICE = {"C.O.": "Colonial Office", "D.O.": "Dominions Office"}
_OFFICE_RE = re.compile(r"\b([CD]\.\s?O\.)(?=[\s,.)]|$)")


def lookup(segment: str, data_dir: str) -> str | None:
    """Return the place string if `segment` names a known place, else None."""
    seg = segment.strip().rstrip(".")
    if not seg:
        return None
    vocab = load(data_dir)
    for cand in (seg, _TRAILING_NOISE.sub("", seg)):
        n = norm(cand)
        if n and n in vocab:
            return ABBREV.get(n, cand)
    return None


def lookup_prefix(segment: str, data_dir: str) -> str | None:
    """Place named at the START of a segment ("Ceylon civ. ser.",
    "G.C. rwy.", "Basutoland mounted police") — longest prefix wins."""
    words = segment.strip().split()
    for k in range(min(4, len(words) - 1), 0, -1):
        hit = lookup(" ".join(words[:k]), data_dir)
        if hit is not None:
            return hit
    return None


def lookup_office(clause: str) -> str | None:
    """Dotted office abbreviations anywhere in the clause ("assigned to the
    C.O.", "retld. to C.O. as ..."). Case-sensitive to avoid 'Field Co.'."""
    m = _OFFICE_RE.search(clause)
    if m:
        return _OFFICE[m.group(1).replace(" ", "")]
    return None
