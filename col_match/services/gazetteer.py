_NORM = re.compile(r"[^a-z]")


def norm(s: str) -> str:
    return _NORM.sub("", s.lower())


@lru_cache(maxsize=4)
def _load_rollup(data_dir: str) -> dict[str, str]:
    """Grounded place -> record-colony roll-up (capitals + KG federation
    membership), generated by build_place_rollup.py from capital_audit +
    empire-evolution-wpcs. {place_norm: colony_norm}."""
    path = Path(data_dir) / "place_rollup.json"
    if not path.exists():
        return {}
    raw = json.loads(path.read_text())
    return {k: norm(v["colony"]) for k, v in raw.items() if v.get("colony")}


@lru_cache(maxsize=1)
def load(data_dir: str) -> set[str]:
    """Normalized vocabulary of known places."""
    vocab: set[str] = set(map(norm, ABBREV.values()))
    vocab.update(ABBREV.keys())
    path = Path(data_dir) / "gazetteer_graph.json"
    if path.exists():
        g = json.loads(path.read_text())
        for name in g.get("colonies", []) + g.get("territories", []):
            vocab.add(norm(name))
    vocab.update(_load_rollup(data_dir))  # grounded capitals/members are valid places
    return vocab


